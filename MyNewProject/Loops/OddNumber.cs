using System;
using System.Collections.Generic;
using System.Text;

namespace MyNewProject.Loops
{
    class OddNumber
    {
        static void Main(string[] args)
        {
           for (int i = 25; i <= 42; i++)
            {
                if(i % 2 == 1)
                {
                    Console.WriteLine(i);
                }
            }
        }
        }
    }


